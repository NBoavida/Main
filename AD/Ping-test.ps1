param($p1, $p2, $p3)
clear
While ($true)
{
	del $p3 > $null
	echo "#############################################################################################" >> $p3
	echo "#############################################################################################"
	echo "$p1 Ping's to $p2 `n" >> $p3
	echo "$p1 Ping's to $p2"
	date >> $p3
	date
	$OutPing = ping $p2 -n $p1 -l 32
	$OutPing
	$OutPing >> $p3
}
